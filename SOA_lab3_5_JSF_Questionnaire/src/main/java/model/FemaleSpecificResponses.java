package model;

public class FemaleSpecificResponses {
    private Integer braSize;
    private Integer cupSize;
    private Integer waistFemaleSize;
    private Integer hipsSize;

    public Integer getBraSize() {
        return braSize;
    }

    public void setBraSize(Integer braSize) {
        this.braSize = braSize;
    }

    public Integer getCupSize() {
        return cupSize;
    }

    public void setCupSize(Integer cupSize) {
        this.cupSize = cupSize;
    }

    public Integer getWaistFemaleSize() {
        return waistFemaleSize;
    }

    public void setWaistFemaleSize(Integer waistFemaleSize) {
        this.waistFemaleSize = waistFemaleSize;
    }

    public Integer getHipsSize() {
        return hipsSize;
    }

    public void setHipsSize(Integer hipsSize) {
        this.hipsSize = hipsSize;
    }
}
